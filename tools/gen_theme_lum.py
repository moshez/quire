#!/usr/bin/env python3
"""Generate ATS2 stadef declarations for WCAG relative luminance.

Usage: python3 tools/gen_theme_lum.py > src/theme_lum.hats

WCAG 2.1 relative luminance formula:
  For each sRGB channel C (0-255):
    C_lin = C/255/12.92                  if C/255 <= 0.04045
    C_lin = ((C/255 + 0.055)/1.055)^2.4  otherwise
  L = 0.2126 * R_lin + 0.7152 * G_lin + 0.0722 * B_lin
  Scaled to 0-10000 integer (L * 10000).
"""

def srgb_to_linear(c):
    """Convert sRGB channel (0-255) to linear (0.0-1.0)."""
    c_norm = c / 255.0
    if c_norm <= 0.04045:
        return c_norm / 12.92
    return ((c_norm + 0.055) / 1.055) ** 2.4

def relative_luminance(r, g, b):
    """WCAG relative luminance, scaled to 0-10000."""
    r_lin = srgb_to_linear(r)
    g_lin = srgb_to_linear(g)
    b_lin = srgb_to_linear(b)
    L = 0.2126 * r_lin + 0.7152 * g_lin + 0.0722 * b_lin
    return round(L * 10000)

def hex_to_rgb(h):
    """Parse hex color to (r, g, b)."""
    h = h.lstrip('#')
    return (int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16))

