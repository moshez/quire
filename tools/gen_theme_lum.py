    hi_rgb = hex_to_rgb(COLORS[hi_name])
    lo_rgb = hex_to_rgb(COLORS[lo_name])
    hi_lum = relative_luminance(*hi_rgb)
    lo_lum = relative_luminance(*lo_rgb)
    # Ensure hi_lum > lo_lum
    if hi_lum < lo_lum:
        hi_lum, lo_lum = lo_lum, hi_lum
    ratio = (hi_lum + 50) / (lo_lum + 50)
    aa_ok = (hi_lum + 50) * 10 >= 45 * (lo_lum + 50)
    print(f'(* {desc}: {ratio:.1f}:1 {"PASS" if aa_ok else "FAIL"} AA *)')
