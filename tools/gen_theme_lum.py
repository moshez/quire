for name, hexcolor in COLORS.items():
    r, g, b = hex_to_rgb(hexcolor)
    lum = relative_luminance(r, g, b)
    print(f'stadef {name}_LUM = {lum}  (* {hexcolor} R={r} G={g} B={b} *)')
    # Also emit RGB channel stadefs
    print(f'stadef {name}_R = {r}')
